package com.cstech;

public class Operator {
     public static void main(String[] args) {
    	 int x = 5;
    	    x += 3;//x = x - 3
    	    System.out.println(x);
    	    x -= 3; //x = x - 3
    	    System.out.println(x);
    	    x%=3; //x=x%3
    	    System.out.println(x);
    	    System.out.println(x<=5 && x<10);
    	    System.out.println(!(x < 5 || x < 10));
	}
}
